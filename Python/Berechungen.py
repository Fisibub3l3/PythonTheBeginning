# Werte und Berechnungen
print("Wert für a eingeben: a= ")
a=input()
print("Wert für b eingeben: b= ")
b=input()

# Berechnung mit Kommastellen
print("c = a + b")
c=float(a)+float(b)

# Ausgabe Resultat mit Kommastellen
print("Resultat mit Kommastellen c= ",c)

# Berechnung ohne Kommastellen
print("c = a + b")
cx=int(float(a)+float(b))

# Ausgabe Resultat ohne Kommastellen
print("Resultat ohne Kommastellen c= ",cx)

# Berechnung gerundet
print("c = a + b")
cy=round(float(a)+float(b))

# Ausgabe Resultat gerundet
print("Resultat ohne Kommastellen c= ",cy)
