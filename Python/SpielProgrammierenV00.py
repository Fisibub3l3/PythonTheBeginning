# Eingabe einer Zahl und Umwandeln zu ganzer Zahl

# Eingabe
print("Bitte eine ganze Zahl eingeben")
x = input()
print("Ihre Eingabe:",x)

# Umwandlung von Eingabe zu ganzer Zahl
xganz = int(x)
print("Als ganze Zahl:",xganz)

# Mit einer Berechnung
xdoppel = xganz * 2
print("Ergebnis verdoppelt:",xdoppel)
